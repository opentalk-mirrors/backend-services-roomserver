use opentalk_types_api_v1::users::PublicUserProfile;
use opentalk_types_common::{users::DisplayName, utils::ExampleData};
use serde::{Deserialize, Serialize};

/// Client context required for a signaling connection
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
#[cfg_attr(feature = "utoipa", derive(utoipa::ToSchema), schema(example = json!(ClientParameters::example_data())))]
pub struct ClientParameters {
    /// Identifier to maintain participant state across reconnections, is generated by the client
    pub client_id: String,

    /// Information about a registered user
    #[serde(flatten)]
    pub kind: ClientKind,
}

#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
#[cfg_attr(feature = "utoipa", derive(utoipa::ToSchema))]
#[serde(rename_all = "snake_case", tag = "kind")]
pub enum ClientKind {
    /// A registered user
    Registered { profile: PublicUserProfile },
    /// A guest participant
    Guest { display_name: DisplayName },
}

impl ExampleData for ClientParameters {
    fn example_data() -> Self {
        Self {
            client_id: "string".into(),
            kind: ClientKind::Registered {
                profile: PublicUserProfile::example_data(),
            },
        }
    }
}

#[cfg(test)]
mod tests {
    use opentalk_types_api_v1::users::PublicUserProfile;
    use opentalk_types_common::{users::DisplayName, utils::ExampleData};
    use serde_json::json;

    use super::ClientKind;
    use crate::client_parameters::ClientParameters;

    #[test]
    fn guest() {
        let value = serde_json::to_value(ClientParameters {
            client_id: "1234".into(),
            kind: ClientKind::Guest {
                display_name: DisplayName::from_str_lossy("test"),
            },
        })
        .unwrap();

        assert_eq!(
            value,
            json!(
                {
                    "client_id": "1234",
                    "kind": "guest",
                    "display_name": "test"
                }
            )
        );
    }

    #[test]
    fn registered() {
        let value = serde_json::to_value(ClientParameters {
            client_id: "1234".into(),
            kind: ClientKind::Registered {
                profile: PublicUserProfile::example_data(),
            },
        })
        .unwrap();

        assert_eq!(
            value,
            json!(
                {
                    "client_id": "1234",
                    "kind": "registered",
                    "profile": {
                        "avatar_url": "https://gravatar.com/avatar/c160f8cc69a4f0bf2b0362752353d060",
                        "display_name": "Alice Adams",
                        "email": "alice@example.com",
                        "firstname": "Alice",
                        "lastname": "Adams",
                        "id": "00000000-0000-0000-0000-0000000a11c3",
                        "title": "",
                    }

                }
            )
        )
    }
}
